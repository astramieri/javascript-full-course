// math methods

console.log(Math.PI); // 3.141592653589793

console.log(Math.trunc(Math.PI));	// returns: 3
console.log(Math.round(Math.PI));	// returns: 3

console.log(Math.floor(Math.PI));	// returns: 3
console.log(Math.ceil(Math.PI));	// returns: 4

console.log(Math.pow(5, 2));	// returns: 25

console.log(Math.min(3, 0.5, 9, -2)); // returns: -2
console.log(Math.max(3, 0.5, 9, -2)); // returns: 9

console.log(Math.random()); // returns: a number between [0, 1)

console.log(Math.floor(Math.random() * 10));
